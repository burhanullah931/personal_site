     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => ['required', 'string', 'max:255']
        ]);

        $Servicetags = new Servicetags;
        $Servicetags->name = $request->input('name');
        $Servicetags->save();

        return redirect()->route('servicetags.index')->with('success', 'New Service Tags is created.');
    }

